        <h1 class="faq-heading"><i class="fa fa-info-circle heading-icon"></i>&nbsp;Základné informácie</h1>
        <p>Program Erasmus+ je zaměřen na vysokoškolské vzdělávání a je určen především pro studenty.Cílem
            programu je podpořit vzájemné vztahy a spolupráci mezi vysokými školami v rámci evropského
            prostoru, zvýšit úroveň transparentnosti a slučitelnosti kvalifikací vysokoškolského vzdělávání
            v Evropě, zkvalitnit jazykovou výuku, informovat o vzdělávacích systémech v různých zemích a dát
            vzdělání evropský rozměr.Základní podmínkou pro zapojení vysokoškolské instituce do programu
            Erasmus+ je přidělení Erasmus Charter for Higher Education (ECHE) od Evropské komise, který UHK
            získala na období 2014-2020.Podprogram Erasmus se zaměřuje na vysoké školství. Je to největší
            program, který podporuje evropskou spolupráci v oblasti vysokoškolského vzdělávání. V současné
            době je do programu aktivně zapojena většina evropských vysokých škol. Fakulta informatiky a
            managementu je v programu zapojena již od akademického roku 1998/1999. Každým rokem se počet
            nabízených pobytů zvyšuje a i nadále se snažíme hledat nové partnerské univerzity v destinacích,
            které v naší nabídce prozatím chybí. V rámci tohoto programu bylo vysláno na střednědobé pobyty
            více než 700 studentů. V současné době máme již 71 partnerských univerzit. Naši studenti mají
            možnost se v rámci studijního pobytu přímo účastnit výuky jednotlivých předmětů a složit
            příslušné zápočty a zkoušky. Mohou poznat nejen styl výuky, ale hlavně mají spoustu příležitostí
            potkat nové přátele, prozkoumat nové prostředí a získat neocenitelné zkušenosti a neopakovatelné
            zážitky.</p>
