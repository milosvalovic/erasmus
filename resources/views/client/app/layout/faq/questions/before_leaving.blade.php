        <h1 class="faq-heading"><i class="fa fa-calendar heading-icon"></i>&nbsp;Čo vybavyť pred
            odchodom</h1>
        <p>Studijní pobyt v zahraniční lze realizovat pouze na základě studijní smlouvy (Learning
            Agreement).
            LA a výpis známek (Transcript of Records) vydaný zahraniční institucí, na které byl studijní
            pobyt realizován, je základem pro uznání zahraničního studia.
            LA obsahuje názvy předmětů, které chce student na zahraniční univerzitě studovat, včetně jejich
            kreditového ohodnocení.
            Student je povinen v zahraničí získat minimálně 15 ECTS kreditů za splnění podmínky úspěšného
            odstudování nejméně 3 odborných předmětů.
            LA musí být uzavřen před odjezdem na zahraniční studijní pobyt.
            LA podepisují v tomto pořadí: student, pověřená osoba na fakultě, koordinátor v zahraničí.
            Podpisem LA pověřená osoba stvrzuje, že se studijním plánem studenta souhlasí a že předměty,
            které má student v LA zapsány a které na zahraniční instituci úspěšně absolvuje, mu budou po
            jeho návratu na FIM uznány.
            Student je povinen použít tento formulář – v případě, že zahraniční instituce také vyžaduje svůj
            vlastní formulář, student vyplní oba.
            V některých případech je LA součástí přihlášky zahraniční partnerské instituce, v tom případě
            student přiloží také LA na formuláři FIM.
            Informace o studijní nabídce student získá na www stránkách zahraniční instituce.
            Některé instituce zasílají studijní katalogy e-mailem přímo nominovaným studentům.
            Vyřízení LA může trvat i několik měsíců (především v letním období), doporučujeme jej proto
            začít vyřizovat v dostatečném předstihu, ideálně zaslat již s přihláškou.</p>
